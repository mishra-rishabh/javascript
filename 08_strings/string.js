/* string in double quotes */
let myName = "Rishabh";
console.log(myName.length);
console.log("First character of myName is: ", myName[0]);   // R

/* string in single quotes */
let someGuy = 'Naveen';
console.log(someGuy.length);
console.log("Last character of someGuy is: ", someGuy[someGuy.length - 1]);     // n

/* template literals */
let boy = "Akshay";
let age = 29;
console.log(`${boy} is ${age} years old.`);     // we use backticks here